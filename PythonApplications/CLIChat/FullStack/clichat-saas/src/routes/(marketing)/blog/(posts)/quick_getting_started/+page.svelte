<p class="lead">Quick "Getting Started"</p>

<h1>Quick Steps</h1>

In summary, <br>
for <em>installing/getting setup</em>, <br>

<ul>
<li>Get a Groq API key (see below)</li>
<li>Purchase and download the installer.</li>
<li>Run the installer in the command line.
<ul>
<li><pre><code class="language-bash">
    $ ./clichat-installer.sh
</code></pre></li>
<li>
Follow the prompts. The only information you need in hand is your Groq API key.
</li>
</ul>
</li>
</ul>

To <b>run</b>,

<ul>
<li> Activate the Python virtual environment you created. For example, if you are on the directory
in which you created the virtual environment, venv (it's just a subdirectory), then run
<pre><code class="language-bash">
    $ source ./venv/bin/activate
</code></pre>
</li>
<li> Run the CLIChat command from anywhere.
<pre><code class="language-bash">
    $ clichat
</code></pre>
</li>
<li> Type <pre><code class="language-bash">.exit</code></pre> to exit CLIChat.
</li>
</ul>



