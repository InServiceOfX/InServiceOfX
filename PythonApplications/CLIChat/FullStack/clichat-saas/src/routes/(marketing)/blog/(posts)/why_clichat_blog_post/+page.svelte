<p class="lead">Why CLIChat?</p>

<p>
I wanted a LLM wrapper where I can chat and get answers from an LLM from my command line as quickly as possible on startup.

I don't want to rely on my browser because I've got 123456789000 tabs open and old or low-powered devices, I don't need to be leaving a huge memory footprint.

</p>

